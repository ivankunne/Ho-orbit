#!/usr/bin/env node
// Geocodes all locations from the h-orbit Google Sheet and writes INSERT SQL.
// Usage:  node scripts/geocode-locations.mjs
// Requires: Node 18+ (uses native fetch). Takes ~2 min due to Nominatim rate-limit.

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname  = path.dirname(fileURLToPath(import.meta.url));
const SHEET_URL  = 'https://docs.google.com/spreadsheets/d/1lhhZOEMY10BOcz7Fewa0WJytoneErgH1/export?format=csv';
const OUTPUT     = path.join(__dirname, '../supabase/scene_locations_data.sql');
const DELAY_MS   = 1200; // Nominatim: max 1 req/sec

// ─── CSV parser (handles quoted fields with inner commas/quotes) ─────────────
function parseCSV(text) {
  return text.split('\n').map(row => {
    const cols = [];
    let inQuote = false, cur = '';
    for (let i = 0; i < row.length; i++) {
      const c = row[i];
      if (c === '"') {
        if (inQuote && row[i + 1] === '"') { cur += '"'; i++; }
        else inQuote = !inQuote;
      } else if (c === ',' && !inQuote) {
        cols.push(cur.trim()); cur = '';
      } else {
        cur += c;
      }
    }
    cols.push(cur.trim());
    return cols;
  });
}

// ─── Geocoding via Nominatim ─────────────────────────────────────────────────
async function geocode(address, city, province) {
  const candidates = [
    `${address}, ${city}, Netherlands`,
    `${city}, ${province}, Netherlands`,
  ];

  for (const q of candidates) {
    if (!q.trim() || q.startsWith(',')) continue;
    const url = `https://nominatim.openstreetmap.org/search?q=${encodeURIComponent(q)}&format=json&limit=1&countrycodes=nl`;
    try {
      const res  = await fetch(url, { headers: { 'User-Agent': 'h-orbit-geocoder/1.0' } });
      const data = await res.json();
      if (data.length > 0) return { lat: parseFloat(data[0].lat), lng: parseFloat(data[0].lon) };
    } catch (e) {
      console.error(`  Error for "${q}":`, e.message);
    }
    await sleep(DELAY_MS);
  }
  return { lat: null, lng: null };
}

const sleep = ms => new Promise(r => setTimeout(r, ms));
const esc   = s  => (s == null || s === '') ? 'NULL' : `'${String(s).replace(/'/g, "''")}'`;

// ─── Main ────────────────────────────────────────────────────────────────────
(async () => {
  console.log('📥  Fetching spreadsheet...');
  const res = await fetch(SHEET_URL);
  if (!res.ok) { console.error('Failed to fetch sheet:', res.status); process.exit(1); }
  const csv = await res.text();

  const rows = parseCSV(csv);
  const locations = [];

  for (const cols of rows) {
    const [province, city, name, address, type, website, notes] = cols;
    if (!province || !name) continue;
    if (province.startsWith('===') || province === 'Provincie') continue;
    if (name.startsWith('===')) continue;
    locations.push({ province, city: city || '', name, address: address || '', type: type || '', website: website || '', notes: notes || '' });
  }

  const eta = Math.ceil(locations.length * DELAY_MS / 60000);
  console.log(`📍  Found ${locations.length} locations. Geocoding (~${eta} min)...\n`);

  const results = [];
  for (let i = 0; i < locations.length; i++) {
    const loc = locations[i];
    process.stdout.write(`[${String(i + 1).padStart(2)}/${locations.length}] ${loc.name} (${loc.city})... `);
    const { lat, lng } = await geocode(loc.address, loc.city, loc.province);
    console.log(lat != null ? `✓ ${lat.toFixed(4)}, ${lng.toFixed(4)}` : '✗ not found');
    results.push({ ...loc, lat, lng });
    if (i < locations.length - 1) await sleep(DELAY_MS);
  }

  const found = results.filter(r => r.lat != null).length;
  console.log(`\n✅  Geocoded ${found}/${results.length} locations.`);
  if (found < results.length) {
    console.log('⚠️   Locations not found (will have NULL coords — add manually if needed):');
    results.filter(r => r.lat == null).forEach(r => console.log(`     – ${r.name}, ${r.city}`));
  }

  const values = results
    .map(l => `  (${esc(l.province)}, ${esc(l.city)}, ${esc(l.name)}, ${esc(l.address)}, ${esc(l.type)}, ${esc(l.website)}, ${esc(l.notes)}, ${l.lat ?? 'NULL'}, ${l.lng ?? 'NULL'})`)
    .join(',\n');

  const sql = `-- Auto-generated by scripts/geocode-locations.mjs on ${new Date().toISOString().slice(0, 10)}
-- ${results.length} locations | ${found} geocoded

INSERT INTO scene_locations (province, city, name, address, type, website, notes, lat, lng) VALUES
${values};
`;

  fs.writeFileSync(OUTPUT, sql, 'utf-8');
  console.log(`\n📄  SQL written to: ${path.relative(process.cwd(), OUTPUT)}`);
  console.log('👉  Next: paste that file into the Supabase SQL editor and run it.');
})();
